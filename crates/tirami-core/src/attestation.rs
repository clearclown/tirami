/// How much faster an attested peer should climb the `AuditTier`
/// ladder, per the Phase 17 Wave 3.1 spec.
pub const ATTESTED_AUDIT_TIER_SPEED_MULTIPLIER: u32 = 5;

/// Whether a newly-received `PriceSignal` from a known-attested
/// peer should be routing-preferred over non-attested equivalents.
/// Operators opt in via `Config::prefer_attested_providers`
/// (wire-up in Wave-3.1-part-2 once the trait surface is wired
/// into `PeerRegistry`).
#[derive(Debug, Clone, Copy, Default, Serialize, Deserialize)]
pub struct AttestationPreferences {
    pub prefer_attested_providers: bool,
}

// ---------------------------------------------------------------------------
// Tests
