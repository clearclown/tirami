//! Phase 17 Wave 3.1 — hardware attestation scaffold.
//!
//! # What this is
//!
//! A pluggable API for hardware-backed attestation of a Tirami node.
//! When a peer can prove (to the quality level their hardware
//! supports) that it's running on a trusted enclave — Apple Secure
//! Enclave, NVIDIA H100 confidential-compute mode, Intel SGX —
//! other nodes grant it:
//!
//! * A priority bit on its [`tirami_core::PriceSignal`]
//!   (`attested: bool = true`).
//! * Faster `AuditTier` promotion (5× the usual rate).
//! * Optional routing preference (operators opt in via config).
//!
//! # What this is NOT
//!
//! Attestation is explicitly **optional premium**. Phase 17 does not
//! require it for any node to participate — the distributed-ethos
//! goal is that a Raspberry Pi can still serve. Attestation is a
//! differentiator for peers who want to earn more, not a gate on
//! participation.
//!
//! # Status
//!
//! Scaffold only in this wave:
//!
//! * [`AttestationProvider`] trait — pluggable producer.
//! * [`AttestationVerifier`] trait — pluggable consumer.
//! * [`AttestationReport`] — wire format.
//! * [`MockAttestationProvider`] / [`MockAttestationVerifier`] —
//!   deterministic pair for tests.
//!
//! Real hardware bindings are deferred:
//!
//! * **Apple Secure Enclave**: the `security-framework` crate binds
//!   the Keychain and SE APIs. Integration will land once we have
//!   an operator testbed on Apple Silicon.
//! * **NVIDIA H100 Confidential Compute**: `nvml-wrapper` exposes
//!   the CC mode bit. The attestation payload is a signed CC report
//!   from the driver.
//! * **Intel SGX / TDX**: via DCAP quoting; Azure / GCP confidential
//!   VMs. Lower priority — most Tirami provider hardware is GPU.

