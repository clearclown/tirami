#[cfg(test)]
mod tests {
    use super::*;

    fn nid(b: u8) -> NodeId {
        NodeId([b; 32])
    }

    #[test]
    fn kind_as_str_roundtrips() {
        assert_eq!(AttestationKind::Mock.as_str(), "mock");
        assert_eq!(AttestationKind::AppleSecureEnclave.as_str(), "apple-se");
        assert_eq!(AttestationKind::NvidiaH100CC.as_str(), "nvidia-h100-cc");
        assert_eq!(AttestationKind::IntelSgx.as_str(), "intel-sgx");
        assert_eq!(AttestationKind::AmdSevSnp.as_str(), "amd-sev-snp");
    }

    #[test]
    fn kind_is_production_excludes_mock() {
        assert!(!AttestationKind::Mock.is_production());
        assert!(AttestationKind::AppleSecureEnclave.is_production());
        assert!(AttestationKind::NvidiaH100CC.is_production());
    }

    #[test]
    fn mock_attest_round_trips_through_verify() {
        let p = MockAttestationProvider;
        let v = MockAttestationVerifier;
        let rep = p.attest(nid(1), 1_000, 60_000);
        assert!(v.verify(&rep, &nid(1), 1_500).is_ok());
    }

    #[test]
    fn verify_rejects_wrong_peer() {
        let p = MockAttestationProvider;
        let v = MockAttestationVerifier;
        let rep = p.attest(nid(1), 1_000, 60_000);
        let err = v.verify(&rep, &nid(2), 1_500).unwrap_err();
        assert!(matches!(err, AttestationError::NodeIdMismatch { .. }));
    }

    #[test]
    fn verify_rejects_stale_report() {
        let p = MockAttestationProvider;
        let v = MockAttestationVerifier;
        let rep = p.attest(nid(1), 1_000, 60_000); // valid until 61 000
        let err = v.verify(&rep, &nid(1), 100_000).unwrap_err();
        assert!(matches!(err, AttestationError::Stale { .. }));
    }

    #[test]
    fn verify_rejects_pre_issuance() {
        let p = MockAttestationProvider;
        let v = MockAttestationVerifier;
        let rep = p.attest(nid(1), 10_000, 60_000);
        // Verifier's clock is behind the issuer's — treat as stale.
        let err = v.verify(&rep, &nid(1), 5_000).unwrap_err();
        assert!(matches!(err, AttestationError::Stale { .. }));
    }

    #[test]
    fn verify_rejects_kind_mismatch() {
        let v = MockAttestationVerifier;
        let mut rep = MockAttestationProvider.attest(nid(1), 1_000, 60_000);
        rep.kind = AttestationKind::NvidiaH100CC;
        let err = v.verify(&rep, &nid(1), 1_500).unwrap_err();
        assert!(matches!(err, AttestationError::KindMismatch { .. }));
    }

    #[test]
    fn verify_rejects_tampered_evidence() {
        let v = MockAttestationVerifier;
        let mut rep = MockAttestationProvider.attest(nid(1), 1_000, 60_000);
        rep.evidence[0] ^= 0xFF;
        let err = v.verify(&rep, &nid(1), 1_500).unwrap_err();
        assert_eq!(err, AttestationError::InvalidEvidence);
    }

    #[test]
    fn verify_rejects_wrong_length_evidence() {
        let v = MockAttestationVerifier;
        let mut rep = MockAttestationProvider.attest(nid(1), 1_000, 60_000);
        rep.evidence.truncate(10);
        let err = v.verify(&rep, &nid(1), 1_500).unwrap_err();
        assert_eq!(err, AttestationError::InvalidEvidence);
    }

    #[test]
    fn report_is_current_reflects_bounds() {
        let p = MockAttestationProvider;
        let rep = p.attest(nid(1), 1_000, 5_000);
        assert!(rep.is_current(1_000)); // lower bound inclusive
        assert!(rep.is_current(6_000)); // upper bound inclusive
        assert!(!rep.is_current(999));
        assert!(!rep.is_current(6_001));
    }

    #[test]
    fn serde_roundtrips_report() {
        let rep = MockAttestationProvider.attest(nid(1), 1_000, 60_000);
        let s = serde_json::to_string(&rep).unwrap();
        let back: AttestationReport = serde_json::from_str(&s).unwrap();
        assert_eq!(back, rep);
    }

    #[test]
    fn audit_tier_multiplier_is_five() {
        // Constant guard — if someone changes the multiplier, a test
        // must alert before the economic balance shifts silently.
        assert_eq!(ATTESTED_AUDIT_TIER_SPEED_MULTIPLIER, 5);
    }

    #[test]
    fn preferences_default_is_off() {
        let p = AttestationPreferences::default();
        assert!(!p.prefer_attested_providers);
    }

    #[test]
    fn provider_kind_matches_report_kind() {
        // The kind the provider advertises MUST match the report it
        // produces — otherwise a verifier can't route to the right
        // verifier implementation.
        let p = MockAttestationProvider;
        let rep = p.attest(nid(1), 1_000, 60_000);
        assert_eq!(p.kind(), rep.kind);
    }
}
