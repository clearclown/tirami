// ---------------------------------------------------------------------------
// AttestationKind
// ---------------------------------------------------------------------------

/// Which hardware backed the attestation. Serialized over the wire
/// so verifiers can pick the right [`AttestationVerifier`].
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum AttestationKind {
    /// `MockAttestationProvider` — deterministic, for tests only.
    Mock,
    /// Apple Secure Enclave — iOS / macOS on Apple Silicon.
    AppleSecureEnclave,
    /// NVIDIA H100 Confidential Compute — dedicated GPU feature.
    NvidiaH100CC,
    /// Intel SGX / TDX (DCAP quote).
    IntelSgx,
    /// AMD SEV-SNP.
    AmdSevSnp,
}

impl AttestationKind {
    pub fn as_str(&self) -> &'static str {
        match self {
            Self::Mock => "mock",
            Self::AppleSecureEnclave => "apple-se",
            Self::NvidiaH100CC => "nvidia-h100-cc",
            Self::IntelSgx => "intel-sgx",
            Self::AmdSevSnp => "amd-sev-snp",
        }
    }

    /// True iff this kind is production-grade (not the mock).
    pub fn is_production(&self) -> bool {
        !matches!(self, Self::Mock)
    }
}

// ---------------------------------------------------------------------------
// AttestationReport — wire format
// ---------------------------------------------------------------------------

/// Self-contained attestation claim. Gossiped alongside `PriceSignal`
/// so any peer can verify without out-of-band communication.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct AttestationReport {
    pub kind: AttestationKind,
    /// Node the attestation is bound to. A verifier MUST reject any
    /// report whose `node_id` does not match the claimed peer.
    pub node_id: NodeId,
    /// Hardware-scheme-specific attestation bytes. For the mock this
    /// is a SHA-256 of `node_id || issued_at_ms`.
    pub evidence: Vec<u8>,
    /// Unix ms when the report was generated by the provider.
    pub issued_at_ms: u64,
    /// Unix ms after which the report is stale. Verifiers reject
    /// reports older than this.
    pub valid_until_ms: u64,
}

impl AttestationReport {
    /// True iff `now_ms` is within the report's validity window.
    pub fn is_current(&self, now_ms: u64) -> bool {
        now_ms >= self.issued_at_ms && now_ms <= self.valid_until_ms
    }
}

// ---------------------------------------------------------------------------
// Errors
// ---------------------------------------------------------------------------

#[derive(Debug, Clone, PartialEq, Eq, thiserror::Error)]
pub enum AttestationError {
    #[error("attestation kind mismatch: report={report:?}, verifier expects {expected:?}")]
    KindMismatch {
        report: AttestationKind,
        expected: AttestationKind,
    },
    #[error("attestation is stale: now_ms={now_ms}, valid_until={valid_until_ms}")]
    Stale {
        now_ms: u64,
        valid_until_ms: u64,
    },
    #[error("attestation node_id mismatch: report={report}, peer={peer}")]
    NodeIdMismatch { report: String, peer: String },
    #[error("attestation evidence failed cryptographic verification")]
    InvalidEvidence,
}

// ---------------------------------------------------------------------------
// Traits
// ---------------------------------------------------------------------------

/// Produces attestation reports. Backed by specific hardware in
/// real implementations; the mock hashes inputs.
pub trait AttestationProvider: Send + Sync {
    fn kind(&self) -> AttestationKind;
    /// Produce a fresh report for the given node at `now_ms`.
    /// `validity_ms` is how long the report should remain valid.
    fn attest(&self, node_id: NodeId, now_ms: u64, validity_ms: u64) -> AttestationReport;
}

/// Verifies attestation reports. Stateless from the API shape; real
/// implementations may cache trust-roots internally.
pub trait AttestationVerifier: Send + Sync {
    fn kind(&self) -> AttestationKind;
    /// Verify that `report` was produced by a legitimate attestation
    /// chain for `expected_peer` at `now_ms`.
    fn verify(
        &self,
        report: &AttestationReport,
        expected_peer: &NodeId,
        now_ms: u64,
    ) -> Result<(), AttestationError>;
}

// ---------------------------------------------------------------------------
// MockAttestationProvider / Verifier
// ---------------------------------------------------------------------------

/// Deterministic provider. Evidence = SHA-256("tirami-mock-attest" ||
/// node_id.0 || issued_at_ms.to_le_bytes()).
#[derive(Debug, Clone, Copy, Default)]
pub struct MockAttestationProvider;

impl MockAttestationProvider {
    const SALT: &'static [u8] = b"tirami-mock-attest";
}

impl AttestationProvider for MockAttestationProvider {
    fn kind(&self) -> AttestationKind {
        AttestationKind::Mock
    }

    fn attest(
        &self,
        node_id: NodeId,
        now_ms: u64,
        validity_ms: u64,
    ) -> AttestationReport {
        let mut h = Sha256::new();
        h.update(Self::SALT);
        h.update(node_id.0);
        h.update(now_ms.to_le_bytes());
        let evidence = h.finalize().to_vec();
        AttestationReport {
            kind: AttestationKind::Mock,
            node_id,
            evidence,
            issued_at_ms: now_ms,
            valid_until_ms: now_ms.saturating_add(validity_ms),
        }
    }
}

/// Matching verifier for [`MockAttestationProvider`]. Re-derives the
/// expected hash and compares byte-wise.
#[derive(Debug, Clone, Copy, Default)]
pub struct MockAttestationVerifier;

impl AttestationVerifier for MockAttestationVerifier {
    fn kind(&self) -> AttestationKind {
        AttestationKind::Mock
    }

    fn verify(
        &self,
        report: &AttestationReport,
        expected_peer: &NodeId,
        now_ms: u64,
    ) -> Result<(), AttestationError> {
        if report.kind != AttestationKind::Mock {
            return Err(AttestationError::KindMismatch {
                report: report.kind,
                expected: AttestationKind::Mock,
            });
        }
        if report.node_id != *expected_peer {
            return Err(AttestationError::NodeIdMismatch {
                report: report.node_id.to_hex(),
                peer: expected_peer.to_hex(),
            });
        }
        if !report.is_current(now_ms) {
            return Err(AttestationError::Stale {
                now_ms,
                valid_until_ms: report.valid_until_ms,
            });
        }
        let mut h = Sha256::new();
        h.update(MockAttestationProvider::SALT);
        h.update(report.node_id.0);
        h.update(report.issued_at_ms.to_le_bytes());
        let expected = h.finalize();
        // Constant-time byte compare.
        if report.evidence.len() != 32 {
            return Err(AttestationError::InvalidEvidence);
        }
        let mut diff: u8 = 0;
        for (a, b) in expected.iter().zip(report.evidence.iter()) {
            diff |= a ^ b;
        }
        if diff != 0 {
            return Err(AttestationError::InvalidEvidence);
        }
        Ok(())
    }
}

