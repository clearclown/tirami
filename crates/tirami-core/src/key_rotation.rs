#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn fresh_identity_has_single_active_epoch() {
        let id = NodeIdentity::with_clock(1_000);
        assert_eq!(id.epoch_count(), 1);
        let active = id.active().unwrap();
        assert_eq!(active.epoch, 0);
        assert_eq!(active.state, KeyState::Active);
        assert_eq!(active.created_at_ms, 1_000);
        assert!(active.revoked_at_ms.is_none());
    }

    #[test]
    fn sign_uses_active_key() {
        let id = NodeIdentity::with_clock(1_000);
        let sig = id.sign(b"hello").unwrap();
        assert_eq!(sig.len(), 64);
        // Verify round-trip against the active epoch's VK.
        let active_vk = id.active().unwrap().verifying_key();
        let sig_arr: [u8; 64] = sig.try_into().unwrap();
        assert!(active_vk
            .verify(b"hello", &ed25519_dalek::Signature::from_bytes(&sig_arr))
            .is_ok());
    }

    #[test]
    fn rotate_moves_current_active_to_revoked_and_creates_fresh_active() {
        let mut id = NodeIdentity::with_clock(1_000);
        let _old_vk = id.active().unwrap().verifying_key_bytes;
        id.rotate(2_000).unwrap();
        assert_eq!(id.epoch_count(), 2);
        // Epoch 0 must be Revoked at 2_000.
        let old = id.epochs().find(|e| e.epoch == 0).unwrap();
        assert_eq!(old.state, KeyState::Revoked);
        assert_eq!(old.revoked_at_ms, Some(2_000));
        // Epoch 1 must be the new Active.
        let new = id.active().unwrap();
        assert_eq!(new.epoch, 1);
        assert_eq!(new.state, KeyState::Active);
        assert_eq!(new.created_at_ms, 2_000);
    }

    #[test]
    fn rotate_generates_distinct_keypair() {
        let mut id = NodeIdentity::with_clock(1_000);
        let old_vk = id.active().unwrap().verifying_key_bytes;
        id.rotate(2_000).unwrap();
        let new_vk = id.active().unwrap().verifying_key_bytes;
        assert_ne!(old_vk, new_vk);
    }

    #[test]
    fn historical_signature_by_revoked_key_is_still_accepted() {
        // Sign at t=500 with epoch 0. Rotate at t=2000. A verifier
        // checking at t=1500 (between sign and rotate) must accept.
        let mut id = NodeIdentity::with_clock(1_000);
        let sig = id.sign(b"past").unwrap();
        id.rotate(2_000).unwrap();
        let ok = verify_historical(&id, b"past", &sig, 1_500).unwrap();
        assert_eq!(ok, 0); // epoch 0's key verified
    }

    #[test]
    fn new_signature_by_revoked_key_is_rejected() {
        let mut id = NodeIdentity::with_clock(1_000);
        // Save the old epoch's signing key BEFORE rotation.
        let old_sig = id.sign(b"future").unwrap();
        id.rotate(2_000).unwrap();
        // Pretend the old-key signature was produced at 3_000 —
        // AFTER the key was revoked. Must reject.
        let err = verify_historical(&id, b"future", &old_sig, 3_000).unwrap_err();
        assert!(matches!(err, KeyRotationError::RevokedKeyUsed { .. }));
    }

    #[test]
    fn new_signature_by_currently_active_key_is_accepted_at_any_time() {
        let mut id = NodeIdentity::with_clock(1_000);
        id.rotate(2_000).unwrap();
        let sig = id.sign(b"now").unwrap();
        // Any t >= current epoch creation is fine.
        let ok = verify_historical(&id, b"now", &sig, 3_000).unwrap();
        assert_eq!(ok, 1);
    }

    #[test]
    fn verify_rejects_malformed_signature_length() {
        let id = NodeIdentity::with_clock(1_000);
        let err = verify_historical(&id, b"m", &[0u8; 10], 1_000).unwrap_err();
        assert!(matches!(err, KeyRotationError::UnknownSigningKey));
    }

    #[test]
    fn verify_rejects_signature_from_foreign_key() {
        let id = NodeIdentity::with_clock(1_000);
        let mut rng = OsRng;
        let foreign = SigningKey::generate(&mut rng);
        let sig = foreign.sign(b"msg");
        let err = verify_historical(&id, b"msg", &sig.to_bytes(), 1_500).unwrap_err();
        assert!(matches!(err, KeyRotationError::UnknownSigningKey));
    }

    #[test]
    fn multiple_rotations_accumulate_history() {
        let mut id = NodeIdentity::with_clock(1_000);
        let sig_0 = id.sign(b"v0").unwrap();
        id.rotate(2_000).unwrap();
        let sig_1 = id.sign(b"v1").unwrap();
        id.rotate(3_000).unwrap();
        let sig_2 = id.sign(b"v2").unwrap();
        assert_eq!(id.epoch_count(), 3);
        // Each historical signature must verify via its own epoch,
        // scoped to the right time.
        assert_eq!(verify_historical(&id, b"v0", &sig_0, 1_500).unwrap(), 0);
        assert_eq!(verify_historical(&id, b"v1", &sig_1, 2_500).unwrap(), 1);
        assert_eq!(verify_historical(&id, b"v2", &sig_2, 3_500).unwrap(), 2);
    }

    #[test]
    fn rotate_twice_correctly_marks_only_the_most_recent_active() {
        let mut id = NodeIdentity::with_clock(1_000);
        id.rotate(2_000).unwrap();
        id.rotate(3_000).unwrap();
        let actives: Vec<_> = id
            .epochs()
            .filter(|e| e.state == KeyState::Active)
            .collect();
        assert_eq!(actives.len(), 1);
        assert_eq!(actives[0].epoch, 2);
    }

    #[test]
    fn serde_roundtrips_identity_including_revoked_epochs() {
        let mut id = NodeIdentity::with_clock(1_000);
        id.rotate(2_000).unwrap();
        let s = serde_json::to_string(&id).unwrap();
        let back: NodeIdentity = serde_json::from_str(&s).unwrap();
        assert_eq!(back.epoch_count(), 2);
        let back_active = back.active().unwrap();
        assert_eq!(back_active.epoch, 1);
        // Revoked epoch survives round-trip with its timestamp.
        let back_old = back.epochs().find(|e| e.epoch == 0).unwrap();
        assert_eq!(back_old.state, KeyState::Revoked);
        assert_eq!(back_old.revoked_at_ms, Some(2_000));
    }
}
